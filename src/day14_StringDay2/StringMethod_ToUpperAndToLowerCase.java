package day14_StringDay2;

public class StringMethod_ToUpperAndToLowerCase {

	public static void main(String[] args) {

		String str = "sharifa";
		System.out.println(str);
		System.out.println(str.toUpperCase());
		System.out.println(str.toLowerCase());
		
		str = str.toUpperCase();
		System.out.println(str);
		
	}

}
